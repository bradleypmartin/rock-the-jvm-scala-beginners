object Collections extends App {
  // Notes
  /*
  - Scala offers both immutable and mutable collections
  - Immutable
    - all these found in scala.collections.immutable package
    - e.g
      - hashset, sortedset
      - hashmap, sortedmap
  - Mutable
    - in scala.collections.mutable
    - e.g.
      - linearseq
      - stringbuilder
      - linkedlist
      - mutablelist

   - traversable
     - base trait for all collections; contains:
     - maps (map, flatmap, collect)
     - conversions (toArray, toList, toSeq)
     - size info (isEmpty, size, nonEmpty)
     - tests (exists, forall)
     - folds (foldLeft, foldRight, reduceLeft, reduceRight)
     - string ops (mkString)

   */

}
